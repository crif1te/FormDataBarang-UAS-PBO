    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
       try {
            PreparedStatement ps = con.prepareStatement("INSERT INTO barang(kd_brg, nm_brg, stok, hrg_bl, hrg_jl) VALUES (?,?,?,?,?)");
            ps.setString(1, kd_brg.getText()); ps.setString(2, nm_brg.getText());
            ps.setString(3, stok.getText()); ps.setString(4, hrg_bl.getText());
            ps.setString(5, hrg_jl.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data tersimpan");
                    } catch (SQLException ex) {
            Logger.getLogger(FormBarang.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Data tidak tersimpan");
        }
        
        kosongkanForm();
        kosongkanTabel();
        try {
            tampilkanDiTabel();
        } catch (SQLException ex) {
            Logger.getLogger(FormBarang.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        simpan.setEnabled(false);
        edit.setEnabled(false);
        hapus.setEnabled(false);
    }//GEN-LAST:event_simpanActionPerformed

