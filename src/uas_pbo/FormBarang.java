    public ArrayList[] getBrgList() throws SQLException{
        //tujuan: mendapatkan data dari database dan disimpan arraylist
        String queryCount = "SELECT COUNT(*) AS c FROM barang";
        Statement st;
        ResultSet rsCount,rs;
        st = con.createStatement();
        rsCount = st.executeQuery(queryCount);
        int  sizeTable = 0;
        while(rsCount.next()){
            sizeTable = rsCount.getInt("c");
            //memanggil alias c dari querycount
    }
         ArrayList[] brgList = new ArrayList[sizeTable];
        //membuat arraylist dengan size sebanyak data di database
        
        
        String query = " SELECT * FROM barang";
        rs = st.executeQuery(query);
        int x=0;
        while(rs.next()){
            brgList[x] = new ArrayList<>();
            brgList[x].add(rs.getString("kd_brg")); brgList[x].add(rs.getString("nm_brg"));
            brgList[x].add(rs.getString("stok")); brgList[x].add(rs.getString("hrg_bl"));
            brgList[x].add(rs.getString("hrg_jl")); 
            x++;
        }
        return brgList;
    }
    
    public void tampilkanDiTabel() throws SQLException{
        ArrayList[] list = getBrgList();
        //buat fungsi getBrgList
        DefaultTableModel model = (DefaultTableModel)tabel.getModel();
       
        Object[] row = new Object[9];
        for(int i=0; i<list.length; i++){
            row[0] = i+1;
            row[1] = list[i].get(0); row[2] = list[i].get(1);
            row[3] = list[i].get(2); row[4] = list[i].get(3);
            row[5] = list[i].get(4); 
            
            model.addRow(row);
        }
    }
    
